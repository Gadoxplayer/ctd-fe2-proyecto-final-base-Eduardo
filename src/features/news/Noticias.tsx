import { useEffect, useState } from "react";
import { obtenerNoticias } from "./fakeRest";
import NewsModal from "./NewsModal";
import {
  TarjetaNoticia,
  FechaTarjetaNoticia,
  DescripcionTarjetaNoticia,
  ImagenTarjetaNoticia,
  TituloTarjetaNoticia,
  ContenedorNoticias,
  ListaNoticias,
  TituloNoticias,
} from "./styled";
import { changeCase, getCurrentTime } from "./dataStyling"


export interface INoticiasNormalizadas {
  id: number;
  titulo: string;
  descripcion: string;
  fecha: number | string;
  esPremium: boolean;
  imagen: string;
  descripcionCorta?: string;
}

const Noticias = () => {
  const [noticias, setNoticias] = useState<INoticiasNormalizadas[]>([]);

  useEffect(() => {
    const obtenerInformacion = async () => {
      const respuesta = await obtenerNoticias();

      const data = respuesta.map((n) => {
        const titulo = changeCase(n.titulo)

        return {
          id: n.id,
          titulo,
          descripcion: n.descripcion,
          fecha: `Hace ${getCurrentTime(n)} minutos`,
          esPremium: n.esPremium,
          imagen: n.imagen,
          descripcionCorta: n.descripcion.substring(0, 100),
        };
      });

      setNoticias(data);
    };

    obtenerInformacion();
  }, []);

  return (
    <ContenedorNoticias>
      <TituloNoticias>Noticias de los Simpsons</TituloNoticias>
      <ListaNoticias>
        {noticias.map((n) => (
          <TarjetaNoticia>
            <ImagenTarjetaNoticia src={n.imagen} />
            <TituloTarjetaNoticia>{n.titulo}</TituloTarjetaNoticia>
            <FechaTarjetaNoticia>{n.fecha}</FechaTarjetaNoticia>
            <DescripcionTarjetaNoticia>
              {n.descripcionCorta}
            </DescripcionTarjetaNoticia>
            <NewsModal id={n.id} titulo={changeCase(n.titulo)} descripcion={n.descripcion} fecha={n.fecha} esPremium={n.esPremium} imagen={n.imagen} ></NewsModal>
          </TarjetaNoticia>
        ))}
      </ListaNoticias>
    </ContenedorNoticias>
  );
};

export default Noticias;
